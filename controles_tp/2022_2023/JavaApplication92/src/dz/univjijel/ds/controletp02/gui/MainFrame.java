    private void btnPrecedentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrecedentActionPerformed
        index--;
        index = index < 0 ? 0 : index;
        
        afficherClient();
    }//GEN-LAST:event_btnPrecedentActionPerformed

    private void btnSuivantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuivantActionPerformed
        index++;
        index = index >= Main.liste.size() ? Main.liste.size() - 1 : index;
        
        afficherClient();
    }//GEN-LAST:event_btnSuivantActionPerformed

    private void btnNouveauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNouveauActionPerformed
        
        if(btnNouveau.getText().equals("Nouveau")){
            txtId.setText("");
            txtNom.setText("");
            txtNTel.setText("");
            txtAdresse.setText("");
        
            btnNouveau.setText("Sauver");
            btnAnnuler.setVisible(true);
        }else{
            Client c = new Client(
                    Integer.parseInt(txtId.getText()),
                    txtNom.getText(),
                    txtNTel.getText(),
                    txtAdresse.getText()
            );
            
            Main.liste.add(c);
            index = Main.liste.size() - 1;
            afficherClient();
            
            btnNouveau.setText("Nouveau");
            btnAnnuler.setVisible(false);
        }
    }//GEN-LAST:event_btnNouveauActionPerformed

    private void btnAnnulerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnnulerActionPerformed
        btnNouveau.setText("Nouveau");
        btnAnnuler.setVisible(false);
        
        afficherClient();
    }//GEN-LAST:event_btnAnnulerActionPerformed

    private void btnMAJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMAJActionPerformed
        if(index > - 1 && index < Main.liste.size()){
            Client c = Main.liste.get(index);
            c.setId(Integer.parseInt(txtId.getText()));
            c.setNomComplet(txtNom.getText());
            c.setnTel(txtNTel.getText());
            c.setAdresse(txtAdresse.getText());
        }
    }//GEN-LAST:event_btnMAJActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        if(index > - 1 && index < Main.liste.size()){
            Main.liste.remove(index);
            index--;
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

