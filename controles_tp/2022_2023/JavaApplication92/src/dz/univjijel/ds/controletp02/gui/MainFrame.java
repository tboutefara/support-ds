    private void afficherClient() {
        if(index > - 1 && index < Main.liste.size()){
            Client c = Main.liste.get(index);
            
            txtId.setText(c.getId() + "");
            txtNom.setText(c.getNomComplet());
            txtNTel.setText(c.getnTel());
            txtAdresse.setText(c.getAdresse());
        }
    }
